export interface CountryAgeRestrictions {
  minimumAge: number;
  requiresParentalConsent: boolean;
  consentAge?: number;
}

export interface CountryActivationRecord {
  isoCode: string;
  name: string;
  region: string;

  // Activation flags
  enabled: boolean;
  beta: boolean;
  publicLaunch: boolean;
  marketingEnabled: boolean;
  registrationEnabled: boolean;
  paymentsEnabled: boolean;
  marketplaceEnabled: boolean;
  aiFeaturesEnabled: boolean;

  // Language & locale
  supportedLanguages: string[];
  defaultLanguage: string;

  // Currency & payments
  supportedCurrencies: string[];
  defaultCurrency: string;
  paymentProviders: PaymentProviderRef[];

  // Compliance
  complianceReviewRequired: boolean;
  sanctionsRestricted: boolean;
  privacyProfile: PrivacyProfile;
  taxProfile: TaxProfile;
  legalNoticeProfile: LegalNoticeProfile;

  // Access rules
  ageRestrictions: CountryAgeRestrictions;

  // Feature flags (country-level overrides)
  featureFlags: {
    hreflangEnabled: boolean;
    sitemapIncluded: boolean;
    betaAccessGate: boolean;
    paymentGate: boolean;
    registrationGate: boolean;
  };
}

