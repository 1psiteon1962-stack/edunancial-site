/**
 * Country Activation Registry
 *
 * Defines the canonical activation model for every country in the Edunancial
 * global platform. Language choice remains independent from country activation.
 *
 * Fields:
 *   enabled              – site is accessible from this country
 *   beta                 – accessible to beta users only (requires enabled:true)
 *   publicLaunch         – fully launched to the public
 *   marketingEnabled     – marketing pages/campaigns targeting this country
 *   registrationEnabled  – new user registration allowed
 *   paymentsEnabled      – payment processing allowed
 *   marketplaceEnabled   – marketplace features available
 *   aiFeaturesEnabled    – AI-powered features available
 *   complianceReviewRequired – legal/compliance team must review before enabling
 *   sanctionsRestricted  – country is subject to international trade sanctions
 */

