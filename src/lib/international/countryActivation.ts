export function getCountryActivation(
  isoCode: string
): CountryActivationRecord | undefined {
  return COUNTRY_ACTIVATION_REGISTRY.find(
    (c) => c.isoCode.toUpperCase() === isoCode.toUpperCase()
  );
}

export function isCountryEnabled(isoCode: string): boolean {
  return getCountryActivation(isoCode)?.enabled ?? false;
}

export function isCountryBeta(isoCode: string): boolean {
  const record = getCountryActivation(isoCode);
  return (record?.enabled ?? false) && (record?.beta ?? false);
}

export function isCountrySanctioned(isoCode: string): boolean {
  return getCountryActivation(isoCode)?.sanctionsRestricted ?? false;
}

export function canInitiatePayment(isoCode: string): boolean {
  const record = getCountryActivation(isoCode);
  if (!record) return false;
  return record.enabled && record.paymentsEnabled && !record.sanctionsRestricted;
}

export function canRegister(isoCode: string): boolean {
  const record = getCountryActivation(isoCode);
  if (!record) return false;
  return record.enabled && record.registrationEnabled && !record.sanctionsRestricted;
}

export function getEnabledCountries(): CountryActivationRecord[] {
  return COUNTRY_ACTIVATION_REGISTRY.filter((c) => c.enabled);
}

export function getBetaCountries(): CountryActivationRecord[] {
  return COUNTRY_ACTIVATION_REGISTRY.filter((c) => c.enabled && c.beta);
}

export function getSanctionedCountries(): CountryActivationRecord[] {
  return COUNTRY_ACTIVATION_REGISTRY.filter((c) => c.sanctionsRestricted);
}

export function getDisabledCountries(): CountryActivationRecord[] {
  return COUNTRY_ACTIVATION_REGISTRY.filter((c) => !c.enabled);
}
