export function getLibraryItem(id: string): LibraryItem | undefined {
  return libraryItems.find((item) => item.id === id);
}

export function searchLibraryItems(params: {
  q?: string;
  type?: string;
  category?: string;
  accessLevel?: string;
  page?: number;
  perPage?: number;
}): { items: LibraryItem[]; total: number } {
  const { q = "", type = "", category = "", accessLevel = "", page = 1, perPage = 12 } = params;

  let results = libraryItems.filter((item) => item.status === "published");

  if (q) {
    const term = q.toLowerCase();
    results = results.filter(
      (item) =>
        item.title.toLowerCase().includes(term) ||
        item.author.toLowerCase().includes(term) ||
        item.description.toLowerCase().includes(term) ||
        item.tags.some((t) => t.toLowerCase().includes(term))
    );
  }

  if (type) {
    results = results.filter((item) => item.type === type);
  }

  if (category) {
    results = results.filter((item) => item.categories.includes(category as never));
  }

  if (accessLevel) {
    results = results.filter((item) => item.accessLevel === accessLevel);
  }

  const total = results.length;
  const start = (page - 1) * perPage;
  const items = results.slice(start, start + perPage);

  return { items, total };
}
